// Criar função que recebe um número e retorna o dobro desse número
function calcularDobro(numero) {
    return numero * 2;

}

// Criar função que recebe tres números e retorna a média deles
function calcularMedia(num1, num2, num3) {
    return (num1 + num2 + num3) / 3;

}

//Criar função que recebe dois números e retorna o maior deles
